def productFunction(url, site_id):
    #url = 'https://usercenter-std.ntalker.com/usercenter/productPoint'
    resp = BaseComponent().get_remote_data(url)
    code = resp.get("code", None) or resp.get("status", None)
    if not code == 200:
        return False
    prod_list = BaseModelHelp().get_prod_function(site_id)
    ret = []
    for prod in prod_list:
        defaultValueSample = ''
        for each in resp['data']:
            if each['productPointid'] == prod['func_list__function__func_code']:
                defaultValueSample = each['defaultValueSample']
                break
        if prod['func_list__function__func_code'] == 'new_ticket':
            pass
        if prod['func_list__function__func_code'] == 'xbot':
            pass
        else:
            switch = prod['func_list__select_value']
            code = prod['func_list__function__func_code']
            str = {"productPointid": code, "parentProductid": "", "defaultValue": defaultValueSample, "switch": switch}
            ret.append(str)
    return ret